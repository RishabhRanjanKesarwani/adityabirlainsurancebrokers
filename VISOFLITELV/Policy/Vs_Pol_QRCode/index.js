function queryParamsStringToObject(queryString) {
  if (queryString === "") {
    return {};
  }

  const rawParams = queryString.substring(1);
  const object = {};

  rawParams.split("&").forEach(rawParam => {
    const paramValue = rawParam.split("=");
    object[paramValue[0]] = paramValue[1];
  });

  return object;
}

